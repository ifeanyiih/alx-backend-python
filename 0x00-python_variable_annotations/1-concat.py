#!/usr/bin/env python3
"""Write a type-annotated function concat that takes a
string str1 and a string str2 as arguments and returns a
concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """Function takes two strins and returns the
    strings concatenated
    Args:
        str1 (str): str1
        str2 (str): str2
    Returns:
        str : a concatenated string
    """
    return str1 + str2
