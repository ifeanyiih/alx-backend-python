#!/usr/bin/env python3
"""Write a type-annotated function to_str that takes a
float n as argument and returns the string
representation of the float
"""


def to_str(n: float) -> str:
    """Function takes a float n and returns
    the string representation of the float
    Args:
        n (float): n
    Returns:
        str : a string representation of float
    """
    return str(n)
